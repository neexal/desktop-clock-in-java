    private void btnClockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClockActionPerformed
        panelTimer.setVisible(false);
        panelClock.setVisible(true);
        Thread clock = new Thread(){
        public void run(){
        try{
            for(;;){
                Calendar now= Calendar.getInstance();
                int hour = now.get(Calendar.HOUR_OF_DAY);
                int minute=now.get(Calendar.MINUTE);
                int second=now.get(Calendar.SECOND);
                
                int year = now.get(Calendar.YEAR);
                int month = now.get(Calendar.MONTH);
                int day = now.get(Calendar.DAY_OF_MONTH);

                lblHour.setText(Integer.toString(hour));
                lblMinute.setText(Integer.toString(minute));
                lblSecond.setText(Integer.toString(second));
                lblDate.setText(day+"/"+month+"/"+year);
                Thread.sleep(1000);
            }
        }catch(Exception e){
            System.out.println(e);
        }
        }
        };
        clock.start();
    }//GEN-LAST:event_btnClockActionPerformed

